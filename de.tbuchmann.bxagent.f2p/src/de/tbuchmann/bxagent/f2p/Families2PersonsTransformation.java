    /**
     * Backward: maps target objects back to source type, assigns to role in intermediate container.
     * Creates intermediate objects as needed and assigns members to roles based on options.
     */
    @SuppressWarnings("unchecked")
    private static void mapRoleBasedTypesBack(
            EObject targetRoot,
            EObject sourceRoot,
            Options options) {
        // Person → FamilyMember via Family
        Map<String, Families.Family> familyLookup = new HashMap<>();
        for (Persons.Person targetObj : (EList<Persons.Person>) targetRoot.eGet(targetRoot.eClass().getEStructuralFeature("persons"))) {
            String intermediateKey = targetObj.getName().contains(", ") ? targetObj.getName().substring(0, targetObj.getName().indexOf(", ")) : targetObj.getName();
            String memberName = targetObj.getName().contains(", ") ? targetObj.getName().substring(targetObj.getName().indexOf(", ") + 2).trim() : "";
            Families.Family family =
                    options.preferExistingFamily() ? familyLookup.getOrDefault(intermediateKey, null) : null;
            if (family == null) {
                family = FamiliesFactory.eINSTANCE.createFamily();
                family.setName(intermediateKey);
                ((EList<EObject>) sourceRoot.eGet(sourceRoot.eClass().getEStructuralFeature("families"))).add(family);
                familyLookup.put(intermediateKey, family);
            }
            Families.FamilyMember member = FamiliesFactory.eINSTANCE.createFamilyMember();
            member.setName(memberName);
            if (targetObj instanceof Persons.Male) {
                if (options.preferParentOverChildRole() && family.getFather() == null) {
                    family.setFather(member);
                } else {
                    family.getSons().add(member);
                }
            } else if (targetObj instanceof Persons.Female) {
                if (options.preferParentOverChildRole() && family.getMother() == null) {
                    family.setMother(member);
                } else {
                    family.getDaughters().add(member);
                }
            }
        }
    }

